    void loadDanhSachSanPham() {
        dtm = (DefaultTableModel) tblDanhSachSP.getModel();
        dtm.setRowCount(0);
        for (SanPhamChiTiet spct : qlspct.getAll()) {
            dtm.addRow(new Object[]{
                spct.getTenSanPham(),
                spct.getTenMauSac(),
                spct.getSize(),
                spct.getTenChatLieu(),
                spct.getGia(),
                spct.getSoLuong()
            });
        }
    }

    void loadDanhSachHoaDon() {
        dtm = (DefaultTableModel) tblHoaDon.getModel();
        dtm.setRowCount(0);
        for (HoaDon hd : qlhd.getAllHoaDon()) {
            dtm.addRow(new Object[]{
                hd.getMaHoaDon(),
                hd.getNgayTao(),
                hd.getTrangThai()
            });
        }
    }

    double thanhTien(Integer soLuong, double donGiaSau) {
        return (soLuong * donGiaSau);
    }

    void loadAllDataHDCT(ArrayList<HoaDonChiTiet> listHDCT) {
        dtm = (DefaultTableModel) tblSP_HD.getModel();
        dtm.setRowCount(0);
        for (HoaDonChiTiet hdct : listHDCT) {
            dtm.addRow(new Object[]{
                hdct.getTenSanPham(),
                hdct.getDonGia(),
                hdct.getSize(),
                hdct.getMauSac(),
                hdct.getTenChatLieu(),
                hdct.getSoLuong(),
                thanhTien(hdct.getSoLuong(), hdct.getDonGia())
            });
        }
    }

